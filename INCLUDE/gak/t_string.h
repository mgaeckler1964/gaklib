class T_STRING : public STRING
{
	const char *nextToken;
	const char *delimiters;


	public:
	T_STRING()
	{
		delimiters = nextToken = NULL;
	};
	explicit T_STRING( char c ) : STRING( c )
	{
		delimiters = nextToken = NULL;
	};
	T_STRING( const char *c ) : STRING( c )
	{
		delimiters = nextToken = NULL;
	};
	T_STRING( const STRING &c ) : STRING( c )
	{
		delimiters = nextToken = NULL;
	};
	T_STRING( const T_STRING &source ) : STRING( source )
	{
		nextToken  = source.nextToken;
		delimiters = source.delimiters;
	};

	// scaning
	T_STRING	getFirstToken( const char *delimiters, bool noTrim=false );
	T_STRING	getNextToken( bool noTrim=false );
	char	getLastDelimiter( void ) const
	{
		return nextToken ? *nextToken : char(0);
	}
	int hasNextToken( void ) const
	{
		return nextToken && *nextToken;
	}

};

