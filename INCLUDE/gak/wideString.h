class uSTRING : public Array<wchar_t>
{
	void privateDecodeUTF8( const STRING &src );
	void fromWstring( const wchar_t *src );
	void fromString( const STRING &src );

	public:
	uSTRING() {};

	uSTRING( const wchar_t	*src )
	{
		fromWstring( src );
	};
	uSTRING &operator = ( const wchar_t *src )
	{
		fromWstring( src );
		return *this;
	}

	uSTRING( const STRING &src )
	{
		fromString( src );
	}
	uSTRING &operator = ( const STRING &src )
	{
		fromString( src );
		return *this;
	}

	operator const wchar_t * () const
	{
		return getDataBuffer();
	}
	operator wchar_t * ()
	{
		return getDataBuffer();
	}

	uSTRING &decodeUTF8( const STRING &src )
	{
		privateDecodeUTF8( src );
		return *this;
	}
	STRING encodeUTF8( void ) const;
	STRING toString( void ) const;
};

