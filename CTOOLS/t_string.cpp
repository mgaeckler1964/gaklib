T_STRING T_STRING::getFirstToken( const char *delimiters, bool noTrim )
{
	size_t	len;
	const char	*cp = (const char*)(*this);
	
	this->delimiters = delimiters;
	if( !cp || !*cp )
	{
		nextToken = NULL;
		return (char*)0;
	}
	else
	{
		len = strcspn( cp, delimiters );

		STRING	result( cp, len );
		nextToken = cp + len;

		if( !noTrim )
			result.stripBlanks();

		return result;
	}
}

T_STRING T_STRING::getNextToken( bool noTrim )
{
	size_t	len;

	if( hasNextToken() )
	{
		nextToken++;
		len = strcspn( nextToken, delimiters );


		STRING result( nextToken, len );
		nextToken += len;

		if( !noTrim )
			result.stripBlanks();

		return result;
	}
	else
	{
		nextToken = NULL;

		return (char*)0;
	}
}

